package com.fis.dto;

public class ProductDTO {
	private int id;
	private String name;
	private String desc;
	private int price;
	private String type;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	@Override
	public String toString() {
		return "ProductDTO [id=" + id + ", name=" + name + ", desc=" + desc + ", price=" + price + ", type=" + type
				+ "]";
	}
	

}
